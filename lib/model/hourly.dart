import 'package:weather_apis/model/rain.dart';
import 'package:weather_apis/model/weather.dart';
class Hourly {
  int? dt;
  double? temp;
  double? feelsLike;
  int? pressure;
  int? humidity;
  double? dewPoint;
  double? uvi;
  int? clouds;
  int? visibility;
  double? windSpeed;
  int? windDeg;
  double? windGust;
  List<Weather>? weather;
  int? pop;
  Rain? rain;

  Hourly({
    this.dt,
    this.temp,
    this.feelsLike,
    this.pressure,
    this.humidity,
    this.dewPoint,
    this.uvi,
    this.clouds,
    this.visibility,
    this.windSpeed,
    this.windDeg,
    this.windGust,
    this.weather,
    this.pop,
    this.rain,
  });

  Hourly.fromJson(Map<String, dynamic> json) {
    dt = json['dt'];
    temp = (json['temp'] is int) ? (json['temp'] as int).toDouble() : json['temp'];
    feelsLike = (json['feels_like'] is int) ? (json['feels_like'] as int).toDouble() : json['feels_like'];
    pressure = json['pressure'];
    humidity = json['humidity'];
    dewPoint = (json['dew_point'] is int) ? (json['dew_point'] as int).toDouble() : json['dew_point'];
    uvi = (json['uvi'] is int) ? (json['uvi'] as int).toDouble() : json['uvi'];
    clouds = json['clouds'];
    visibility = json['visibility'];
    windSpeed = (json['wind_speed'] is int) ? (json['wind_speed'] as int).toDouble() : json['wind_speed'];
    windDeg = json['wind_deg'];
    windGust = (json['wind_gust'] is int) ? (json['wind_gust'] as int).toDouble() : json['wind_gust'];
    if (json['weather'] != null) {
      weather = <Weather>[];
      json['weather'].forEach((v) {
        weather!.add(Weather.fromJson(v));
      });
    }
    pop = json['pop'];
    rain = json['rain'] != null ? Rain.fromJson(json['rain']) : null;
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['dt'] = dt;
    data['temp'] = temp;
    data['feels_like'] = feelsLike;
    data['pressure'] = pressure;
    data['humidity'] = humidity;
    data['dew_point'] = dewPoint;
    data['uvi'] = uvi;
    data['clouds'] = clouds;
    data['visibility'] = visibility;
    data['wind_speed'] = windSpeed;
    data['wind_deg'] = windDeg;
    data['wind_gust'] = windGust;
    if (weather != null) {
      data['weather'] = weather!.map((v) => v.toJson()).toList();
    }
    data['pop'] = pop;
    if (rain != null) {
      data['rain'] = rain!.toJson();
    }
    return data;
  }
}

